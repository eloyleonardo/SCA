package domain;

public class Nd {

    private int codigo;
    private String descricao;

    public Nd() {
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int val) {
        this.codigo = val;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String val) {
        this.descricao = val;
    }
}

