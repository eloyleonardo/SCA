package domain;

public class TipoSaida {

    private int codigo;
    private String nome;

    public TipoSaida() {
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int val) {
        this.codigo = val;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String val) {
        this.nome = val;
    }
}

